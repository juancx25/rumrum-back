#ifndef MODOS
#define MODOS

/**
 * @file Modos.hpp
 * @brief Definición del conjunto de modos para un LED o buzzer.
 */

/**
 * @enum Modos
 * @brief Enumeración que define los posibles modos.
 */
enum Modos
{
    APAGADO,   ///< LED/buzzer apagado.
    ENCENDIDO, ///< LED/buzzer encendido.
    TITILANDO  ///< LED/buzzer titilando.
};

#endif
